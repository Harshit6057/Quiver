USING (auth.uid() = target_id);

-- Message read receipts
CREATE TABLE IF NOT EXISTS public.message_receipts (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  message_id bigint REFERENCES public.messages(id) ON DELETE CASCADE NOT NULL,
  user_id uuid REFERENCES public.users(id) ON DELETE CASCADE NOT NULL,
  read_at timestamp with time zone,
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE (message_id, user_id)
);

ALTER TABLE public.message_receipts ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Participants can read receipts" ON public.message_receipts;
DROP POLICY IF EXISTS "Users can upsert own receipts" ON public.message_receipts;

CREATE POLICY "Participants can read receipts"
ON public.message_receipts FOR SELECT
USING (
  EXISTS (
    SELECT 1
    FROM public.messages m
    JOIN public.conversation_participants cp ON cp.conversation_id = m.conversation_id
    WHERE m.id = message_receipts.message_id
      AND cp.user_id = auth.uid()
  )
);

CREATE POLICY "Users can upsert own receipts"
ON public.message_receipts FOR INSERT
WITH CHECK (
  auth.uid() = user_id
  AND EXISTS (
    SELECT 1
    FROM public.messages m
    JOIN public.conversation_participants cp ON cp.conversation_id = m.conversation_id
    WHERE m.id = message_receipts.message_id
      AND cp.user_id = auth.uid()
  )
);

CREATE POLICY "Users can update own receipts"
ON public.message_receipts FOR UPDATE
USING (auth.uid() = user_id);

